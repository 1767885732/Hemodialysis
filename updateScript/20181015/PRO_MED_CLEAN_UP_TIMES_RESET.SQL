CREATE OR REPLACE PROCEDURE PRO_MED_CLEAN_UP_TIMES_RESET(HEMOID IN VARCHAR2) AS
  BASE_CLEAN_UP_TIMES NUMBER(5);
BEGIN
  SELECT T.CLEAN_UP_TIMES
    INTO BASE_CLEAN_UP_TIMES
    FROM (SELECT T.*
            FROM MED_CURE_MAIN T
           WHERE T.HEMODIALYSIS_ID = HEMOID
             AND T.CURE_STATUS <> '4'
           ORDER BY T.CURE_CREATE_DATE) T
   WHERE ROWNUM = 1;

  FOR RECORD_ROW IN (SELECT ROWNUM AS ROW_NUM, T.*
                       FROM (SELECT T.*
                               FROM MED_CURE_MAIN T
                              WHERE T.HEMODIALYSIS_ID = HEMOID
                                AND T.CURE_STATUS <> '4'
                              ORDER BY T.CURE_CREATE_DATE) T) LOOP
    UPDATE MED_CURE_MAIN T
       SET T.CLEAN_UP_TIMES = BASE_CLEAN_UP_TIMES + (RECORD_ROW.ROW_NUM - 1)
     WHERE T.CURE_ID = RECORD_ROW.CURE_ID;
    COMMIT;
  END LOOP;

END PRO_MED_CLEAN_UP_TIMES_RESET;
